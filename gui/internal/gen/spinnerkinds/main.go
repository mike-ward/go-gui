// Command spinnerkinds scans gui/assets/svg-spinners/*.svg and
// emits gui/svg_spinner_kinds_gen.go with a typed enum plus a
// data lookup array. Run via `go generate ./gui/...`.
package main

import (
	"bytes"
	"fmt"
	"go/format"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"unicode"
)

const (
	assetsDir = "gui/assets/svg-spinners"
	outFile   = "gui/svg_spinner_kinds_gen.go"
	prefix    = "SvgSpinner"
)

func main() {
	entries, err := os.ReadDir(assetsDir)
	if err != nil {
		log.Fatalf("read %s: %v", assetsDir, err)
	}
	var names []string
	for _, e := range entries {
		n := e.Name()
		if e.IsDir() || !strings.HasSuffix(n, ".svg") {
			continue
		}
		names = append(names, n)
	}
	sort.Strings(names)
	if len(names) == 0 {
		log.Fatalf("no svg assets found in %s", assetsDir)
	}

	var buf bytes.Buffer
	fmt.Fprintf(&buf, "// Code generated by gui/internal/gen/spinnerkinds;"+
		" DO NOT EDIT.\n")
	fmt.Fprintf(&buf, "// Enum values are iota-assigned in sorted-filename"+
		" order. Adding or removing\n")
	fmt.Fprintf(&buf, "// assets renumbers the enum; callers must use the"+
		" named constants, not\n")
	fmt.Fprintf(&buf, "// numeric values.\n\n")
	fmt.Fprintln(&buf, "package gui")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "import _ \"embed\"")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "// SvgSpinnerKind selects a built-in SVG spinner.")
	fmt.Fprintln(&buf, "type SvgSpinnerKind uint16")
	fmt.Fprintln(&buf)
	fmt.Fprintln(&buf, "// SvgSpinnerKind values, one per embedded asset.")
	fmt.Fprintln(&buf, "const (")
	for i, n := range names {
		id := identFromFilename(n)
		if i == 0 {
			fmt.Fprintf(&buf, "\t%s SvgSpinnerKind = iota\n", id)
			continue
		}
		fmt.Fprintf(&buf, "\t%s\n", id)
	}
	fmt.Fprintln(&buf, ")")
	fmt.Fprintln(&buf)
	fmt.Fprintf(&buf, "const svgSpinnerCount = %d\n\n", len(names))

	for _, n := range names {
		id := identFromFilename(n)
		fmt.Fprintf(&buf, "//go:embed assets/svg-spinners/%s\n", n)
		fmt.Fprintf(&buf, "var svgSpinnerData%s string\n\n", id)
	}

	fmt.Fprintln(&buf,
		"var svgSpinnerData = [svgSpinnerCount]string{")
	for _, n := range names {
		id := identFromFilename(n)
		fmt.Fprintf(&buf, "\t%s: svgSpinnerData%s,\n", id, id)
	}
	fmt.Fprintln(&buf, "}")
	fmt.Fprintln(&buf)

	fmt.Fprintln(&buf,
		"var svgSpinnerName = [svgSpinnerCount]string{")
	for _, n := range names {
		id := identFromFilename(n)
		base := strings.TrimSuffix(n, ".svg")
		fmt.Fprintf(&buf, "\t%s: %q,\n", id, base)
	}
	fmt.Fprintln(&buf, "}")

	src, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("gofmt: %v\n----\n%s", err, buf.String())
	}
	if err := os.WriteFile(outFile, src, 0o644); err != nil {
		log.Fatalf("write %s: %v", outFile, err)
	}
	fmt.Fprintf(os.Stderr, "wrote %s (%d kinds)\n", outFile, len(names))
}

// identFromFilename turns "3-dots-bounce.svg" into
// "SvgSpinner3DotsBounce".
func identFromFilename(name string) string {
	base := strings.TrimSuffix(filepath.Base(name), ".svg")
	segments := strings.FieldsFunc(base, func(r rune) bool {
		return r == '-' || r == '_' || r == '.'
	})
	var b strings.Builder
	b.WriteString(prefix)
	for _, seg := range segments {
		b.WriteString(titleSegment(seg))
	}
	return b.String()
}

// titleSegment title-cases a single hyphen-delimited segment.
// Digits pass through; the outer prefix guarantees a letter
// start on the full identifier.
func titleSegment(s string) string {
	if s == "" {
		return ""
	}
	runes := []rune(s)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}
